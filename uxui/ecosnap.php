                <div class="content_obj">
                    <h4>Reflection</h4>
                    <p>
                    Although our group had a rough beginning, I enjoyed the whole process of designing an interface and I am also satisfied with the outcome we presented. Throughout this project, I learned more about what I am capable of and what I should improve in terms of skills. The Interface Design course inspired me to aspire to become a UI designer specifically. <br><br>
                    There are several ideas to improve EcoSnap in the future. As one of the SPEC committees suggested, EcoSnap could record the kinds and the amount of waste collected for the statistics. This analysis would motivate the users by showing their contributions in actual numbers. EcoSnap could also allow the users to connect with other users to form a community that shares common interests. During the user testing, our group learned that some participants felt EcoSnap was like a small community that gives a sense of belonging. Allowing users to connect with each other would increase participation as well. 

