                <div class="content_obj">
                    <h4>Process</h4>
                    <p>
                    Our group aimed to create a platform that allows users to easily report and form and/or participate in cleanup groups. We tried to keep the processes as simple as possible.
                    </p>

                    <h4 class="content_obj_subsection">Research & Interviews</h4>
                    <p>
                    We focused on researching and interviewing the potential audiences for the first two weeks. Our group not only was able to conduct several interviews with the potential users but also I, along with two other members of our team (Seansky and Celine) participated in a meeting with the waste committee at the Society Promoting Environmental Protection (SPEC). We learned what the stakeholder looks for from cleanup events (they care a lot about the environment and the whole process of cleanup including how the collected wastes would be processed) and the feedback, especially on the weaknesses of EcoSnap from their point of view. (Overall, the committees liked our interface idea!)
                    </p>

                    <h4 class="content_obj_subsection">Wireflows, Mockups & Prototype</h4>
                    <p>
                    Based on the research, interviews, and the meeting with SPEC, our group refined our interface idea and finalized a total of five features. The two core features are reporting and participating. The users can report dump areas and/or participate in cleanup groups using EcoSnap. There are three additional features; a calendar to view cleanup schedules, an About page to learn more about EcoSnap and how the users can protect the environment using EcoSnap, and lastly, a Profile page for the users to be more engaged by earning badges that show the usersâ€™ past contributions. The users could also download or share their volunteer certificates in the profile feature. I was in charge of the About page to introduce EcoSnap and its goals. The whole team participated in the wire flows and mockups of the entire app together. Then, while I worked on basic interactions on Figma with other members, one member worked on more complicated interactions on Protopie.
                    </p>

                    <h4 class="content_obj_subsection">User Testing & Final Polishing</h4>
                    <p>
                    I conducted 6 user tests after we finished designing all features. Based on the feedback our team members and I got after user testing, I improved the visuals while other members fixed minor errors in the features. Finally, our team presented EcoSnap in the last class.
                    </p>

