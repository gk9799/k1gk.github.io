            <h3>Overview</h3>
                <p>
                In an Interface Design course, I proposed and designed an interface as a member of a team of 5 (Seansky, Celine, Lucy, Eva, and myself). Our team chose sustainability as our domain after research and came up with an app called EcoSnap. <br><br>
                For a given 7 weeks, we did research on sustainability and cleanup volunteers, interviewed the potential audience, ideated based on the research and the interviews, refined our ideas, and designed an interface. Our team worked synchronously and asynchronously mostly on Figma and also on Protopie for the prototype for the last couple of weeks. <br><br>
                Most of us contributed in almost all areas but worked more in-depth in our specialties. For example, I participated in the research, content, wire flows, mockups, and basic interactions (on Figma) of the app with other members, but on top of that, I mainly worked with the visuals and the presentation slides for each week.
                </p>

