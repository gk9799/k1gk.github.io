                    <h4 class="content_obj_subsection">Schedule Management</h4>
                    <p>
                    The interface Design course offered me to experience all the processes of designing an app. The challenge our group faced that triggered us to struggle with the entire project schedule was to come up with an interesting and innovative idea that does not commonly exist. We ended up choosing our idea at the end of the second week so we were tight on schedule. The designs for the first few weeks did not look good due to the reason so it was frustrating for all of us. However, as our group dedicated more time to this project to catch up with the schedule, our app improved. At this phase, I finished my part early and experimented on the colour scheme for EcoSnap. I also proposed several logo designs and navigation bar designs and created icons while other members worked on their wire flows. Finalizing visual elements was important for us as we needed to create mockups. Choosing colours and designing visuals took some time and required patience, but it felt so satisfying after finalizing them.
